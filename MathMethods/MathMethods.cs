namespace Arithmetic
{
    class MathMethods
    {
        public static int Add(int num1, int num2)
        {
            return num1 + num2;
        }
        public static int Sub(int num1, int num2)
        {
            return num1 - num2;
        }

        public static int Mul(int num1, int num2)
        {
            return num1 * num2;
        }

        public static int Div(int num1, int num2)
        {
            return num1 / num2;
        }

    }
}
